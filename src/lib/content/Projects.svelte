<h2>Orbital</h2>

<br />

<p>
	I was curious about satellites and wanted to know how many there were just above my head. This is
	were I entered a rabbit hole of space debris, satellite tracking and orbital propagations.
</p>

<br />

<p>
	Orbital is a web application that displays the position of satellites in real-time. It uses the
	Two-Line Element Set (TLE) format and the SGP4 propagation algorithm to get the position of the
	satellites.
</p>

<br />

<p>Made using React and ThreeJS. A second version is in the works using Rust and Svelte!</p>

<br />

<a href="https://orbital.matteo.ooo">Go check out the satellites!</a>

